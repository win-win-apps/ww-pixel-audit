// Map a platform string (as it appears in scan results / wizard config) to an
// icon key. Falls back to the generic glyph for anything we don't recognize.
const PLATFORM_TO_KEY: Array<[RegExp, IconKey]> = [
  [/^meta/i, "meta"],
  [/google\s*ads/i, "googleads"],
  [/google\s*analytics/i, "googleanalytics"],
  [/google\s*tag\s*manager/i, "googletagmanager"],
  [/tiktok/i, "tiktok"],
  [/klaviyo/i, "klaviyo"],
  [/pinterest/i, "pinterest"],
  [/snap/i, "snapchat"],
  [/reddit/i, "reddit"],
  [/custom\s*pixel|shopify/i, "shopify"],
];

export function platformIconKey(platform: string): IconKey {
  for (const [pattern, key] of PLATFORM_TO_KEY) {
    if (pattern.test(platform)) return key;
  }
  return "generic";
}

export interface PlatformIconProps {
  platform: string;
  size?: number;
  monochrome?: boolean; // ignore brand color, render in currentColor
  title?: string;
}

export function PlatformIcon({
  platform,
  size = 20,
  monochrome = false,
  title,
}: PlatformIconProps) {
  const key = platformIconKey(platform);
  const def = ICONS[key];
  const labelText = title ?? def.label;
  const baseStyle: React.CSSProperties = {
    flex: "0 0 auto",
    display: "inline-block",
    verticalAlign: "middle",
  };

  if (def.kind === "single") {
    const fill = monochrome ? "currentColor" : def.color;
    return (
      <svg
        role="img"
        aria-label={labelText}
        viewBox="0 0 24 24"
        width={size}
        height={size}
        style={baseStyle}
        xmlns="http://www.w3.org/2000/svg"
      >
        <title>{labelText}</title>
        <path fill={fill} d={def.path} />
      </svg>
    );
  }

  // multi-color: render each path with its own brand color, unless caller asked
  // for monochrome (e.g. a dark UI strip), in which case fall back to currentColor.
  return (
    <svg
      role="img"
      aria-label={labelText}
      viewBox={def.viewBox}
      width={size}
      height={size}
      style={baseStyle}
      xmlns="http://www.w3.org/2000/svg"
    >
      <title>{labelText}</title>
      {def.paths.map((p, i) => (
        <path key={i} fill={monochrome ? "currentColor" : p.color} d={p.d} />
      ))}
    </svg>
  );
}
